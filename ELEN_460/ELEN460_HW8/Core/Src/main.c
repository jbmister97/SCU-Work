/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM15_Init();
  MX_TIM16_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim15, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim16, TIM_CHANNEL_1);
  HAL_GPIO_WritePin(FWD_POS_PIN, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(REV_POS_PIN, GPIO_PIN_RESET);
  
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    
    // 10 ms scheduler
    if(count_10ms_flag) {
      count_10ms_flag = false;
      
      // Read potentiometer voltage
      HAL_ADC_Start_IT(&hadc1);
      
      // Read switch positions
      motorEnable = HAL_GPIO_ReadPin(MOTOR_PWR_SW_PIN);
      motorDir = HAL_GPIO_ReadPin(MOTOR_DIR_SW_PIN);
      servoEnable = HAL_GPIO_ReadPin(SERVO_PWR_SW_PIN);
      motorSpeed = adcValue << 4;
      
      if(!motorEnable) { // Inverted logic
        if(motorDir == FORWARD) {       // Forward
          // First turn off reverse control transistors
          HAL_GPIO_WritePin(REV_POS_PIN, GPIO_PIN_RESET);
          Set_Motor_Rev_Speed(0);
          
          // Turn on forward control transistors
          HAL_GPIO_WritePin(FWD_POS_PIN, GPIO_PIN_SET);
          Set_Motor_Fwd_Speed(motorSpeed);
        }
        else {  // Reverse
          // First turn off foward control transistors
          HAL_GPIO_WritePin(FWD_POS_PIN, GPIO_PIN_RESET);
          Set_Motor_Fwd_Speed(0);
          
          // Turn on reverse control transistors
          HAL_GPIO_WritePin(REV_POS_PIN, GPIO_PIN_SET);
          Set_Motor_Rev_Speed(motorSpeed);
        }
      }
      else {
        // Turn all control transistors off
        HAL_GPIO_WritePin(FWD_POS_PIN, GPIO_PIN_RESET);
        Set_Motor_Fwd_Speed(0);
        HAL_GPIO_WritePin(REV_POS_PIN, GPIO_PIN_RESET);
        Set_Motor_Rev_Speed(0);
      }
      
    
    }
    
    // 1.5 sec scheduler
    if(count_1_5sec_flag) {
      count_1_5sec_flag = false;
      if(!servoEnable) { // Inverted logic
        switch(servoState) {
        case SERVO_STATE_ZERO_POSITION:
          Set_Servo_Position(SERVO_ZERO_POSITION_VALUE);
          if(servoDir == LEFT) {servoState = SERVO_STATE_NEG_90_POSITION;}
          else {servoState = SERVO_STATE_POS_90_POSITION;}
          break;
        case SERVO_STATE_NEG_90_POSITION:
          Set_Servo_Position(SERVO_NEG_90_POSITION_VALUE);
          servoState = SERVO_STATE_ZERO_POSITION;
          servoDir = RIGHT;
          break;
        case SERVO_STATE_POS_90_POSITION:
          Set_Servo_Position(SERVO_POS_90_POSITION_VALUE);
          servoState = SERVO_STATE_ZERO_POSITION;
          servoDir = LEFT;
          break;
        }
      }
      else {Set_Servo_Position(0);}
    }
    
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

