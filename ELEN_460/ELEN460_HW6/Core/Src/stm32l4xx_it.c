/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  counter_10mS--;
  if(counter_10mS == 0) {
    counter_10mS_Flag = true;
    counter_10mS = COUNT_10MS_VALUE;
  }
  
  counter_1S--;
  if(counter_1S == 0) {
    counter_1S_Flag = true;
    counter_1S = COUNT_1S_VALUE;
  }
  
  counter_2_5S--;
  if(counter_2_5S == 0) {
    counter_2_5S_Flag = true;
    counter_2_5S = COUNT_2_5S_VALUE;
  }
  /* USER CODE END SysTick_IRQn 1 */
}

